void MoverPlaneta (int x, int y)
{
	int i,j,r,g,b,k;
	struct Puntos T;
	srand(time(NULL));
	T=planeta();
	k=0;
	do
	{
		for(i=0;i<41;i++)
		{
			//for(j=k;j<(41+k)%81;j++)
			for(j=0;j<40;j++)
			{
				switch(T.M[i][j+k])
				{	
				case 1://color azul
						r=0;
						g=128;
						b=255;
						break;		
				case 2://color rojo
						r=255;
						g=0;
						b=0;
						break;
			case 3://color verde
						r=72;
						g=228;
						b=77;
						break;
				case 4:
						r=162;
						g=87;
						b=168;
						break;
				}
				putpixel(x+j,y+i,COLOR(r,g,b));
			}
			//con esto borramos las esquinas del rectangulo!!
			setcolor(0);
			for(r=0;r<10;r++)
			{
				circle(x+20,y+20,21+r);
				/*Tarea, hay que quitarle los puntos*/
			//	rectangle(400,300,400+36+r,300+36+r);
			}
		}	
		delay(10);
		k++;
	/*	if(k<80)
			k++;
		if(k==80)
			k=0;*/
	}while(k<80);	
}
